#!/bin/bash
# ============================================================
# 配置生成器模板
# 用法: bash scripts/gen-config.sh <build-dir>
# 根据项目需求自定义 CONFIG_* / PACKAGE_* 设置
# ============================================================
set -e

BUILD_DIR="${1:-build}"
CONFIG_FILE="${BUILD_DIR}/.config"

cat > "$CONFIG_FILE" << 'CONFIGEOF'
# ─── 目标平台 ──────────────────────────────────────────
# 在这里填写平台配置

# ─── 核心组件 ──────────────────────────────────────────
# 在这里填写组件选择

# ─── 额外工具 ──────────────────────────────────────────
# 在这里填写工具选择
CONFIGEOF

echo "✅ .config 已生成: $CONFIG_FILE ($(wc -l < "$CONFIG_FILE") 行)"